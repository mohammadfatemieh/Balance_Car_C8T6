/*************************************************************************
*函数名：  PWM_Frequency_Set()
*函数功能：设置某定时器产生的PWM频率
*入口参数：TIMX ：定时器号    			@arg ： TIM1    定时器1
					 Frequency ：频率    			@arg ： 2    2KHz
*返回值：  无
*作者：    康滢
*时间：		 2017.7.31
**************************************************************************/
void PWM_Frequency_Set(TIM_TypeDef *TIMX, uint32_t Frequency)
{

	TIM_HandleTypeDef mtimx;
	Frequency = Frequency>1000 ? 1000:Frequency<1?1:Frequency;	//1<=Frequency<=1000
	mtimx.Instance = TIMX;
	if((int)(SystemCoreClock/2/(mtimx.Instance->ARR+1)) == Frequency*1000)
	{			//原本频率和想设置的相同，跳过配置
		return;
	}
	mtimx.Init.Prescaler = 1;
	mtimx.Init.CounterMode = TIM_COUNTERMODE_UP;
	mtimx.Init.Period = (int)(SystemCoreClock/2000/Frequency)-1;
	mtimx.Init.ClockDivision = TIM_CLOCKDIVISION_DIV1;
	mtimx.Init.AutoReloadPreload = TIM_AUTORELOAD_PRELOAD_DISABLE;
	HAL_TIM_Base_Init(&mtimx);
	return;
}

/*************************************************************************
*函数名：  PWM_Ratio_Set()
*函数功能：设置某定时器某通道产生的PWM占空比
*入口参数：htim ：定时器号    			@arg ： &htim1    定时器1
					 Channel ：通道号     		@arg ： TIM_CHANNEL_1    通道1
					 ratio ：占空比    				@arg ： 6789    占空比为67.89%
*返回值：  无
*作者：    康滢
*时间：		 2017.7.31
*参考：		 http://blog.csdn.net/u013429988/article/details/60905860
**************************************************************************/
void PWM_Ratio_Set(TIM_HandleTypeDef *htim, uint32_t Channel, uint32_t ratio)
{
	uint32_t pulse = (int)((float)(htim->Instance->ARR+1)/100*(float)ratio/100);//为确保最高占空比精度
	switch(Channel)
	{
		case TIM_CHANNEL_1:
		{
			htim->Instance->CCR1 = pulse;
			return;
		}
		case TIM_CHANNEL_2:
		{
			htim->Instance->CCR2 = pulse;
			return;
		}
		case TIM_CHANNEL_3:
		{
			htim->Instance->CCR3 = pulse;
			return;
		}
		case TIM_CHANNEL_4:
		{
			htim->Instance->CCR4 = pulse;
			return;
		}
		default:	return;
	}
}
